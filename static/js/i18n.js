const defaultLocale = "ru";
let activeLocale = defaultLocale;

function resolveLocale(locale) {
  return translations[locale] ? locale : defaultLocale;
}

export function setLocale(locale) {
  activeLocale = resolveLocale(locale);
  return activeLocale;
}

export function getLocale() {
  return activeLocale;
}

export function t(key, params = {}, locale = activeLocale) {
  const resolvedLocale = resolveLocale(locale);
  const dictionary = translations[resolvedLocale] || {};
  const template = dictionary[key] ?? translations[defaultLocale][key] ?? key;
  return template.replace(/\{(\w+)\}/g, (match, token) => {
    const value = params[token];
    if (value === undefined || value === null) {
      return match;
    }
    return String(value);
  });
}

export function formatNumber(value, options = {}, locale = activeLocale) {
  const resolvedLocale = resolveLocale(locale);
  return new Intl.NumberFormat(resolvedLocale, options).format(value);
}

export function formatPercent(value, locale = activeLocale) {
  if (value === null || value === undefined) {
    return "‚Äî";
  }
  const resolvedLocale = resolveLocale(locale);
  return new Intl.NumberFormat(resolvedLocale, {
    style: "percent",
    minimumFractionDigits: 0,
    maximumFractionDigits: 1,
  }).format(value / 100);
}

export { translations, defaultLocale };
